# "%" use to find the remainder of a division operation. In this case, 34 divided by 5 gives a quotient of 6 and a remainder of 4. Therefore, the output of print(a%b) will be 4.

# "*" is the multiplication operator in Python. It is used to multiply two numbers together. For example, if you have a = 3 and b = 4, then a * b would give you 12.

# "-" is the subtraction operator in Python. It is used to subtract one number from another. For example, if you have a = 10 and b = 4, then a - b would give you 6.

# "+" is the addition operator in Python. It is used to add two numbers together. For example, if you have a = 5 and b = 3, then a + b would give you 8.

# "/" is the division operator in Python. It is used to divide one number by another. For example, if you have a = 10 and b = 2, then a / b would give you 5.0.

# "!=" is the not equal to operator in Python. It is used to compare two values and returns True if they are not equal, and False if they are equal. For example, if you have a = 5 and b = 3, then a != b would return True, while if a = 5 and b = 5, then a != b would return False.

